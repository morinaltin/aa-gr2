package Detyra7;

public class CommonManager {
    public int manager_id;
    public String manager_name;
    public int distance_to_emp1;
    public int distance_to_emp2;

    public CommonManager(int manager_id, String manager_name, int distance_to_emp1, int distance_to_emp2) {
        this.manager_id = manager_id;
        this.manager_name = manager_name;
        this.distance_to_emp1 = distance_to_emp1;
        this.distance_to_emp2 = distance_to_emp2;
    }
}
